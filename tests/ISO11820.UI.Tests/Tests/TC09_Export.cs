    [Fact(DisplayName = "12.4 目录自动创建")]
    public void Export_AutoCreateDirectories()
    {
        StartAndLoginViaSignal();
        var window = GetMainWindow(); Assert.NotNull(window);

        var timestamp = DateTime.Now.ToString("HHmmss");
        RunFullTestCycle(window, $"EXP-{timestamp}", "T05");
        Thread.Sleep(2000);

        // 验证目录结构
        var expectedTestDataDir = Path.Combine(_baseDirectory, "TestData", $"EXP-{timestamp}");
        var expectedReportsDir = Path.Combine(_baseDirectory, "Reports");

        _output.WriteLine($"  TestData 目录: {(Directory.Exists(expectedTestDataDir) ? "已创建" : "未创建")}");
        _output.WriteLine($"  Reports 目录: {(Directory.Exists(expectedReportsDir) ? "已创建" : "未创建")}");

        Capture("12.4_目录自动创建");
        _output.WriteLine("✅ 目录自动创建验证完成");
    }

    // ═══════════════════════════════════════════════════
    //  辅助方法
    // ═══════════════════════════════════════════════════

    private void RunFullTestCycle(FlaUI.Core.AutomationElements.Window window, string productId, string testId)
    {
        CreateNewTest(window, productId, testId);
        window = GetMainWindow() ?? window;
        ClickButtonBySignal("click_start_heating");
        WaitStrategies.TryWaitForLabelText(window, "当前状态", "就绪", TimeSpan.FromSeconds(30));
        ClickButtonBySignal("click_start_recording");
        Thread.Sleep(5000);
        ClickButtonBySignal("click_stop_recording");
        Thread.Sleep(2000);
    }

