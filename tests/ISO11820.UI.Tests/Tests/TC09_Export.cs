        var reportDir = Path.Combine(_baseDirectory, "Reports");
        if (Directory.Exists(reportDir))
        {
            var xlsxFiles = Directory.GetFiles(reportDir, "*.xlsx", SearchOption.AllDirectories);
            _output.WriteLine($"  Excel 文件数: {xlsxFiles.Length}");
            foreach (var f in xlsxFiles)
            {
                var fi = new FileInfo(f);
                _output.WriteLine($"    {f} ({fi.Length} bytes)");
            }
        }
        else
        {
            _output.WriteLine($"  报告目录不存在: {reportDir}");
        }

        Capture("12.2_Excel导出");
        _output.WriteLine("✅ Excel 导出测试完成");
    }

